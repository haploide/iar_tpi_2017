    private int[][] obtenerConfInicial()
    {
        int[][] confInicial = new int[3][3];

        for (int i = 0; i < 3; i++)
        {
            for (int j = 0; j < 3; j++)
            {
                confInicial[i][j] = Integer.parseInt(confInicialArray[i][j].getText());
            }
        }

        return confInicial;
    }

